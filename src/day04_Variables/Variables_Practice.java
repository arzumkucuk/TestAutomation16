package day04_Variables;

public class Variables_Practice {

    public static void main(String[] args) {

        byte num1=100;
        short num2=10000;
        float num3=3.5f; // verilen degerin sonuna 'f' koyarsak float formatinda yazilabilir YADA direkt 'double' yaz
        double num4=2.5;
        long num5=9999999999l; // sonuna L yada l koymazsan hata verir ve sistem 'int' olarak okur.
        int num6=850000;

        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
        System.out.println(num5);
        System.out.println(num6);
    }
}
