package day03_Variables;
// yeni bir satira gecmek icin \n komutunu kullaniyoruz.
public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello everyone");
        System.out.println("My name is Arzum, I am from Hawaii");

        System.out.println("=====================================");

        System.out.println("Hello Everyone\nMy name is Arzum, I am from Hawaii");

        System.out.println("====================================");

    // \t:
        System.out.println("\tToday I learned Java");
        System.out.println(" it was so much fun");

        System.out.println("====================================");
    //  \\: \ 2 slas kullan tek slas al
        System.out.println("/");
        System.out.println("\\"); // ONEMLI-tek ters slas yaparsan kod okunmuyor. 2 adet kullan ve tek slas cikart.

        System.out.println("====================================");

        System.out.println("     ^");
        System.out.println("    / \\");
        System.out.println("   /   \\");
        System.out.println("  /     \\");
        System.out.println(" /       \\");
        System.out.println(" ---------");


        System.out.println("====================================");

        System.out.println("My name is 'Arzum' ");
        System.out.println("My name is \'Arzum\'");
        System.out.println("\"Arzum\" "); // double quote yaptik








    }


}

