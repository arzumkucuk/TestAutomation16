package day06_ArithmeticOperators;

public class DivisionCalculator {
    public static void main(String[] args) {


        int num1= 10;
        int num2= 3;
        int divide= 10/3;
        int remainder= 10%3;
        System.out.println(num1 + " divide by "+ num2+ " is equal to "+divide+" with a remainder of "+ remainder);

        System.out.println();


        int a1 = 20;
        int a2=6;
        int divide1= 20/6;
        int remainder2= 20%6;

        System.out.println(a1+" divide by "+ a2 + " is equal to "+ divide1+ " with a remainder of "+ remainder2);
    }
}
