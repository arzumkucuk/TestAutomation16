package day06_ArithmeticOperators;

public class EscapeSequences_Recap {
    public static void main(String[] args) {

        System.out.println("\tHello Everyone\nHello Batch 21\nToday I learned new \'programming\' language \"Java\"\n");

        System.out.println("My favorite quote is:\n\t\t \"Winter is Coming\"\n\t\t\t\t-Ned Stark");

        System.out.println("====================================================");

        System.out.println("\\");
        System.out.println("\n\t Hi Cybertek \n\t\tToday is great day!\nIam happy to be part of \\ ");
    }
}
