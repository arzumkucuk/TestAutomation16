package Odevler;

public class ForLoop {

    public static void main(String[] args) {

        for (int i=1; i<=5; i++){
            System.out.println("Cybertek");

        }
         for (int i=0; i<50; i+=2){
             System.out.print(i+" ");
         }
          System.out.println();

         for (int i=50; i>=0; i--){
             System.out.print(i+" ");
         }

        System.out.println();

         for (char ch='A'; ch<='Z'; ch++){
             System.out.print(ch+" ");
         }





    }
}
