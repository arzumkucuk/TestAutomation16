package day18_For_Loop;

public class ForLoop_Practice {

    public static void main(String[] args) {


        for (int i = 1; i <= 5; i++){ // i: 1,2,3,4,5 <= 5

            System.out.println("Cybertek Batch 22");
        }

        System.out.println("==============================");

        for (int i=1; i <= 10; i++){

            System.out.print(i+ " ");
        }
        System.out.println();

        System.out.println("===============================");

        for (int i=100; i >= 1; i-- ){

            System.out.print(i+" ");
        }


    }
}
